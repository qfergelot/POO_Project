package troupes;

public class Troupe {
	private int cout_production;
	private int temps_production;
	private int vitesse;
	private int vie;
	private int degats;
	
	public Troupe(int cout_production, int temps_production, int vitesse, int vie, int degats) {
		this.cout_production = cout_production;
		this.temps_production = temps_production;
		this.vitesse = vitesse;
		this.vie = vie;
		this.degats = degats;;
	}
	
	
}
